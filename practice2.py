number1 = input("Type a number: ")
number2 = input("Type a 2nd number: ")

print(number1 + "+" + number2 + "=" + str(int(number1) + int(number2)))
print(number1 + "-" + number2 + "=" + str(int(number1) - int(number2)))
print(number1 + "*" + number2 + "=" + str(int(number1) * int(number2)))
print(number1 + "/" + number2 + "=" + str(int(number1) / int(number2)))
